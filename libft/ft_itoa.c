#include "libft.h"

char		*ft_itoa(int n)
{
	char	*a;
	int		i;
	int		len;

	i = 1;
	len = n;
	while (len /= 10)
		i++;
	if (n < 0)
		i++;
	if (!(a = (char*)malloc(sizeof(char) + (i))))
		return (NULL);
	a[i] = '\0';
	if (n == 0)
		a[0] = 48;
	if (n < 0)
		a[0] = 45;
	while (n)
	{
		a[--i] = n < 0 ? -(n % 10 - 48) : n % 10 + 48;
		n /= 10;
	}
	return (a);
}
